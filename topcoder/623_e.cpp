class NotTwo{
  public:
    int maxStones(int width,int height){
      int ans=0;
      if(width<height) swap(width,height);
      rep(i,height){
        rep(j,width){ 
          if((i%4==0||i%4==1)&&(j%4==0||j%4==1)) ans++;
          if((i%4==2||i%4==3)&&(j%4==2||j%4==3)) ans++;
         
        }
      }

      return ans;
    }

};
